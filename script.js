const defaults = {
  title: "Utada Hikaru",
  metaDescription:
    "An artistic multilingual profile page about Utada Hikaru, spanning childhood songwriting, landmark albums, and her cross-cultural legacy."
};

const elements = {
  statRow: document.querySelector("#stat-row"),
  motifList: document.querySelector("#motif-list"),
  timelineList: document.querySelector("#timeline-list"),
  songGrid: document.querySelector("#song-grid"),
  legacyGrid: document.querySelector("#legacy-grid"),
  buttons: document.querySelectorAll(".lang-btn")
};

function fillText(locale) {
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (content[locale][key]) {
      node.textContent = content[locale][key];
    }
  });
}

function renderStats(locale) {
  elements.statRow.innerHTML = content[locale].stats
    .map(
      (item) => `
        <article class="stat-card">
          <span class="stat-value">${item.value}</span>
          <span class="stat-label">${item.label}</span>
        </article>
      `
    )
    .join("");
}

function renderMotifs(locale) {
  elements.motifList.innerHTML = content[locale].motifs.map((item) => `<li>${item}</li>`).join("");
}

function renderTimeline(locale) {
  elements.timelineList.innerHTML = content[locale].timeline
    .map(
      (item) => `
        <article class="timeline-item">
          <span class="timeline-year">${item.year}</span>
          <h3>${item.title}</h3>
          <p class="timeline-copy">${item.copy}</p>
        </article>
      `
    )
    .join("");
}

function renderSongs(locale) {
  elements.songGrid.innerHTML = content[locale].songs
    .map(
      (item) => `
        <article class="song-card reveal">
          <span class="song-era">${item.era}</span>
          <h3>${item.title}</h3>
          <p>${item.text}</p>
          <div class="song-tracks">
            ${item.tracks.map((track) => `<span class="song-track">${track}</span>`).join("")}
          </div>
        </article>
      `
    )
    .join("");
}

function renderLegacy(locale) {
  elements.legacyGrid.innerHTML = content[locale].legacy
    .map(
      (item) => `
        <article class="legacy-card reveal">
          <span class="legacy-label">${item.label}</span>
          <h3>${item.title}</h3>
          <p>${item.text}</p>
        </article>
      `
    )
    .join("");
}

function updateButtons(locale) {
  elements.buttons.forEach((button) => {
    const active = button.dataset.lang === locale;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });
}

function setLanguage(locale) {
  const safeLocale = content[locale] ? locale : "zh";

  document.documentElement.lang = safeLocale;
  document.title = content[safeLocale].pageTitle || defaults.title;
  fillText(safeLocale);
  renderStats(safeLocale);
  renderMotifs(safeLocale);
  renderTimeline(safeLocale);
  renderSongs(safeLocale);
  renderLegacy(safeLocale);
  updateButtons(safeLocale);
  localStorage.setItem("utada-language", safeLocale);

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute("content", defaults.metaDescription);
  }
}

elements.buttons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

const userLanguage = localStorage.getItem("utada-language");
const browserLanguage = navigator.language.startsWith("ja")
  ? "ja"
  : navigator.language.startsWith("zh")
    ? "zh"
    : "en";

setLanguage(userLanguage || browserLanguage);
